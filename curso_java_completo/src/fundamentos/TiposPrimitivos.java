package fundamentos;

public class TiposPrimitivos {
	public static void main(String[] args) {
		/**
		 * Númericos
		 * Byte -> 1 byte 
		 * Short -> 2 bytes
		 * int -> 4 bytes
		 * long -> 8 bytes
		 */
		
		/**
		 * Pontos Flutuantes
		 * Float -> 4 bytes
		 * Double -> 8 bytes
		 */
		
		/**
		 * Char -> Conjunto de caracteres
		 * Boolean -> Verdadeiro ou falso
		 */
		
		
		// informação de um funcionário
		byte anosDeEmpresa = 10;
		short numeroDeVoos = 32_431;
		int id = 1_398_123;
		long pontosAcumulados = 5_341_432_342L;
		
		float salario = 5_399.32F;
		double horasTrabalhadas = 4_342_543.12;
		
		boolean ferias = false;
		
		char sexo = 'F';
	}
}
