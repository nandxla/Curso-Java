package fundamentos;

public class Wrappers {
	public static void main(String[] args) {
		// Versão objeto do tipo primitivo
		// terão métodos e atributos atrelados a eles
		
		Byte b = 100;
		Short s = 1000;
		Integer i = 10000;
		Long l = 100000L;
		
		System.out.println(i.doubleValue());
		
	}
}
