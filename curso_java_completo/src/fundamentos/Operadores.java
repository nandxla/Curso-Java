package fundamentos;

public class Operadores {
	public static void main(String[] args) {
		// unário
		int a = 0;
		a++;
		a++;
		
		System.out.println(a);
		
		// binário
		int b = 10;
		int c = 39;
		double d = 1.31;
		
		System.out.println(b + c);
		System.out.println(b - c);
		System.out.println(b * c);
		System.out.println(b / c);
		
		System.out.println(d / (double) b);
		
		// ternário
	}
}
